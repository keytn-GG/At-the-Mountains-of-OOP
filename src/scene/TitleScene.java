package scene;

import game.GameContext;

/**
 * タイトルを表示するシーン
 */
public class TitleScene extends BaseScene implements Scene {
    public TitleScene(GameContext ctx) {
        super(ctx);
    }
    
    /**
     * このSceneのplay()
     * AAを表示した後に、Enterで続行待ちする
     */
    @Override
    public Scene play() {
        gm.say("""
                
                A T   T H E   M O U N T A I N S   O F   O O P
                     ---------------------------------
                     OOP設計を学ぶ、超圧縮！CoC『狂気の山脈にて』
                     
                                   ^    ^
                                ^^^^  ^^^^^
                             ^^^^^^^^  ^^^^^^^^^
                          ^^^^^^^^^^^  ^^^^^^^^^^^^^
                       ^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^
                   ^^^^^^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^
                ^^^^^^^^^^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^^^^^
             ^^^^^^^^^^^^^^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
          ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
       ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
      ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
    
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~

                """);
        gm.say("--- Enterでゲームを開始する ---");
        gm.waitEnter();
        
        return new IntroScene(ctx);
    }
}
